export const generateDarkModeColors = (themeSetting: any) => {
    themeSetting.forEach((setting: any) => {
        if (setting.value.startsWith('#')) {
            const color = Color(setting.value);
            if (color.luminosity() > 0.5) {
                setting.darkValue = color.darken(0.8).hex();
            } else {
                setting.darkValue = color.darken(0.2).hex();
            }
        }
    });
};

